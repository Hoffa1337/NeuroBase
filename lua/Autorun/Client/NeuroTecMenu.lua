function WorldViewMDL(pl)

local objMDL = pl:GetNetworkedString( "objectMDL" )

	//Show a ghost of the entity at spawnpoint coords.
	for k,v in pairs( ents.GetAll() ) do

	local c = v:GetClass()
	local PointExist = ( string.find( c, "sent_spawnpoint" ) != nil )
			
			if PointExist then
			ViewPoint = v:GetPos()
			pl:SetNetworkedVector( "SpawnPoint", ViewPoint )
					if Preview then
						Preview:Remove()
					end
--						Preview:SetModel( pl:GetNetworkedString( "objectMDL", planemdl[1] ) )			
--						Preview:SetSkin( pl:GetNetworkedString( "Skin", 0 ) )			
--					else
						local Preview=ents.Create( "prop_physics" )
					//	Preview:SetModel( pl:GetNetworkedString( "objectMDL" ) )			
						Preview:SetModel( objMDL )			
						Preview:SetPos( ViewPoint + Vector( 0, 0, 5 ) )
						Preview:SetSkin( pl:GetNetworkedString( "Skin", 0 ) )			
						Preview:SetColor( Color(255,255,255, 200) )
						Preview:Spawn()
						Preview:SetParent( ent )
						pl:SetNetworkedEntity("objectVW", Preview)
--					end

			end
	end		

end

